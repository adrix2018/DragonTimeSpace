#define DECLARE_PACKET_NAME( name )	m_mapPacketName.insert(PacketEnumNameMap::value_type(name, #name))

typedef std::map< unsigned short, std::string > PacketEnumNameMap;
PacketEnumNameMap m_mapPacketName;


std::string GetPacketName(unsigned short wOpCode)
{
	if (!packetsLoaded)
		InitPacketNames();

	PacketEnumNameMap::iterator iter = m_mapPacketName.find(wOpCode);

	if (m_mapPacketName.end() != iter)
	{
		return iter->second.c_str();
	}
	else
		return NULL;
}

